var a = 5;
console.log("Initial value of a:", a); // 5

console.log("Post-Increment (a++):", a++); // It will print first 5 then increases to 6 but does not print 6
console.log("Value after Post-Increment:", a); //Value= 6

console.log("Pre-Increment (++a):", ++a); //First Increases from 6 to 7 then it prints 7

var b = 10;
console.log("Value of b:", b); // 

console.log("Post-Decrement (b--):", b--); // It will print first 10 then decreases to 9 but does not print 9
console.log("Value after Post-Decrement:", b); // Value=9

console.log("Pre-Decrement (--b):", --b); // First it decreases from 9 to 8 and then prints 8
